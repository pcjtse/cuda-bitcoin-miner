#define ROTRIGHT(a,b) (((a) >> (b)) | ((a) << (32-(b))))
#define CH(x,y,z) (((x) & (y)) ^ (~(x) & (z)))
#define MAJ(x,y,z) (((x) & (y)) ^ ((x) & (z)) ^ ((y) & (z)))
#define EP0(x) (ROTRIGHT(x,2) ^ ROTRIGHT(x,13) ^ ROTRIGHT(x,22))
#define EP1(x) (ROTRIGHT(x,6) ^ ROTRIGHT(x,11) ^ ROTRIGHT(x,25))
#define SIG0(x) (ROTRIGHT(x,7) ^ ROTRIGHT(x,18) ^ ((x) >> 3))
#define SIG1(x) (ROTRIGHT(x,17) ^ ROTRIGHT(x,19) ^ ((x) >> 10))

void sha256_transform(uint32_t* state, const uint8_t* block) {
    uint32_t a, b, c, d, e, f, g, h, t1, t2, m[64];
    int i, j;

    // Prepare message schedule
    for (i = 0, j = 0; i < 16; ++i, j += 4)
        m[i] = (block[j] << 24) | (block[j + 1] << 16) | (block[j + 2] << 8) | (block[j + 3]);
    for (; i < 64; ++i)
        m[i] = SIG1(m[i - 2]) + m[i - 7] + SIG0(m[i - 15]) + m[i - 16];

    // Initialize working variables
    a = state[0];
    b = state[1];
    c = state[2];
    d = state[3];
    e = state[4];
    f = state[5];
    g = state[6];
    h = state[7];

    // Main loop
    for (i = 0; i < 64; ++i) {
        t1 = h + EP1(e) + CH(e,f,g) + K[i] + m[i];
        t2 = EP0(a) + MAJ(a,b,c);
        h = g;
        g = f;
        f = e;
        e = d + t1;
        d = c;
        c = b;
        b = a;
        a = t1 + t2;
    }

    // Update state
    state[0] += a;
    state[1] += b;
    state[2] += c;
    state[3] += d;
    state[4] += e;
    state[5] += f;
    state[6] += g;
    state[7] += h;
}

void sha256_init(SHA256_CTX* ctx) {
    ctx->state[0] = 0x6a09e667;
    ctx->state[1] = 0xbb67ae85;
    ctx->state[2] = 0x3c6ef372;
    ctx->state[3] = 0xa54ff53a;
    ctx->state[4] = 0x510e527f;
    ctx->state[5] = 0x9b05688c;
    ctx->state[6] = 0x1f83d9ab;
    ctx->state[7] = 0x5be0cd19;
    ctx->count = 0;
}

void sha256_update(SHA256_CTX* ctx, const uint8_t* data, size_t len) {
    size_t i;
    size_t index = ctx->count % 64;
    ctx->count += len;

    for (i = 0; i < len; ++i) {
        ctx->buffer[index++] = data[i];
        if (index == 64) {
            sha256_transform(ctx->state, ctx->buffer);
            index = 0;
        }
    }
}

void sha256_final(SHA256_CTX* ctx, uint8_t* hash) {
    uint8_t bits[8];
    uint32_t index = ctx->count % 64;
    uint32_t padLen = (index < 56) ? (56 - index) : (120 - index);
    uint64_t count_bits = ctx->count * 8;

    // Add padding
    uint8_t padding[64] = {0x80};
    sha256_update(ctx, padding, padLen);

    // Add length
    for (int i = 0; i < 8; i++) {
        bits[i] = (count_bits >> ((7 - i) * 8)) & 0xff;
    }
    sha256_update(ctx, bits, 8);

    // Generate hash
    for (int i = 0; i < 8; i++) {
        hash[i * 4] = (ctx->state[i] >> 24) & 0xff;
        hash[i * 4 + 1] = (ctx->state[i] >> 16) & 0xff;
        hash[i * 4 + 2] = (ctx->state[i] >> 8) & 0xff;
        hash[i * 4 + 3] = ctx->state[i] & 0xff;
    }
}

void double_sha256(const uint8_t* input, size_t length, uint8_t* output) {
    SHA256_CTX ctx;
    uint8_t temp_hash[SHA256_HASH_SIZE];
    
    // First SHA256
    sha256_init(&ctx);
    sha256_update(&ctx, input, length);
    sha256_final(&ctx, temp_hash);
    
    // Second SHA256
    sha256_init(&ctx);
    sha256_update(&ctx, temp_hash, SHA256_HASH_SIZE);
    sha256_final(&ctx, output);
} 